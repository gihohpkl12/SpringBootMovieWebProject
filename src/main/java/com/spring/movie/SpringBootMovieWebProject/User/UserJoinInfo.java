package com.spring.movie.SpringBootMovieWebProject.User;

public class UserJoinInfo {

	private String id;
	private String firstPw;
	private String secondPw;
	private String email;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFirstPw() {
		return firstPw;
	}
	public void setFirstPw(String firstPw) {
		this.firstPw = firstPw;
	}
	public String getSecondPw() {
		return secondPw;
	}
	public void setSecondPw(String secondPw) {
		this.secondPw = secondPw;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
